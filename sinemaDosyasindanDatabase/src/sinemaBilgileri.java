
public class sinemaBilgileri {
	
	
	private String filmismi;
	private String filmturu;
	private String filmyapimi;
	private String filmsure;
	
	
	
	public sinemaBilgileri(String filmismi, String filmturu, String filmyapimi, String filmsure) {
		super();
		this.filmismi = filmismi;
		this.filmturu = filmturu;
		this.filmyapimi = filmyapimi;
		this.filmsure = filmsure;
	}



	public String getFilmismi() {
		return filmismi;
	}



	public void setFilmismi(String filmismi) {
		this.filmismi = filmismi;
	}



	public String getFilmturu() {
		return filmturu;
	}



	public void setFilmturu(String filmturu) {
		this.filmturu = filmturu;
	}



	public String getFilmyapimi() {
		return filmyapimi;
	}



	public void setFilmyapimi(String filmyapimi) {
		this.filmyapimi = filmyapimi;
	}



	public String getFilmsure() {
		return filmsure;
	}



	public void setFilmsure(String filmsure) {
		this.filmsure = filmsure;
	}
	
	
	
	

}
